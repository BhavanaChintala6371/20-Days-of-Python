num = 3.14
print(type(num))

# Arithmetic operators
print(3 + 2)
print(3 - 2)
print(3 / 2)
print(3 * 2)
print(3 // 2)
print(3 ** 2) 
print(3 % 2)

# Assignment operators
num = 5
num = num + 5
print(num)
num += 5
print(num)

num = num * 5
print(num)
num *= 5
print(num)

# built-in functions to work with numbers
print(abs(-3))
print(round(3.175))
print(round(3.175, 1))

# Comparison operators
num_1 = 3
num_2 = 4
print(num_1 == num_2)
print(num_1 != num_2)
print(num_1 >= num_2)
print(num_1 <= num_2)
print(num_1 < num_2)
print(num_2 > num_1)

# casting
number_1 = "100"
number_2 = "200"
print(number_1 + number_2)

number_1 = int(number_1)
number_2 = int(number_2)
print(number_1 + number_2)